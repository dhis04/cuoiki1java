package Model;

public class Booth
{
    private String idBooth;
    private String nameBooth;
    public String getIdBooth() {
        return idBooth;
    }
    public void setIdBooth(String idBooth) {
        this.idBooth = idBooth;
    }
    public String getNameBooth() {
        return nameBooth;
    }
    public void setNameBooth(String nameBooth) {
        this.nameBooth = nameBooth;
    }
    @Override
    public String toString() {
        return  this.nameBooth ;
    }
    

}
